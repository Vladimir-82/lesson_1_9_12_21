string = 'school'

print(f'третий символ - {string[2]}')
print(f'предпоследний символ - {string[-2]}')
print(f'первые пять символов - {string[:5]}')
print(f'все кроме последних двух символов - {string[:-2]}')
print(f'четные символы - {string[::2]}')
print(f'нечетные символы - {string[1::2]}')
print(f'символы в обратном порядке - {string[::-1]}')
print(f'символы через один в обратном порядке - {string[::-2]}')
print(f'длина строки - {len(string)}')